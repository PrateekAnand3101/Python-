#arithmetic
print(5 + 2)
print(5 - 2)
print(5 * 2)
print(5 / 2) #returns decimal value
print(5 // 2) #returns int value
print(5 % 2) #returns remainder
print(5 ** 2) # power operator
#relational
print(3 > 2)
print(3 < 2)
print(3 >= 2)
print(3 <= 2)
print(3 == 2)
print(3 != 2)
#logical
print(2 > 3 or 2 < 3) 
print(2 > 3 and 2 < 3) 
print(not 2 > 3) 
